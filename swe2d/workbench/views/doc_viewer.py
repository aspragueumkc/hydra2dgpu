"""In-dock documentation hub — cross-document search and viewer for the CFD Inspector.

Shows an index of all available guides.  Search queries span every indexed
document simultaneously and display results grouped by source document.
Click any doc or search result to open the full document with a "Home"
button to return to the index.

Usage
-----
    from swe2d.workbench.doc_viewer import DocHubWidget

    hub = DocHubWidget(parent=self)
    inspector_tabs.addTab(hub, "Help")
"""

